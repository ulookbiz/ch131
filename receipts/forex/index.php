<div class='title'>
    <h3>Key terms</h3>
</div>  

<div class='info'>
  <span class='mark'>Indicator Lag</span> - Significant moves in the forex market occur before a technical indicator provides a signal<br>
  <span class='mark'>MACD</span> - Moving Average Convergence Divergence<br>
  <span class='mark'>Pips</span> - A “pip” is a percentage in point. One pip is equal to 1/100th of 1 percent<br>
  <span class='mark'>Candlestick</span> - Displays the opening and closing price in the charts; also general trend<br>
  <span class='mark'>Price Action Signal</span> - This is the signal naked forex traders use to find the perfect entry at right price<br>
  <span class='mark'>Market Biofeedback</span> - A psychological, behavioral, and trading response to the market price after a trade has been entered<br>
  <span class='mark'>Back-testing</span> - Common method used to test a trading system through historical data<br>
  <span class='mark'>Hindsight Bias</span> - The tendency to overestimate the predictability of the market after the future outcome is known<br>
  <span class='mark'>Zones</span> - Those spots on the chart where price has repeatedly reversed<br>
  <span class='mark'>Minor Zones</span> - A minor zone is nothing more than a support and resistance zone on the next-lower time frame chart<br>
  <span class='mark'>Breakouts</span> - When the market has traded beyond it’s zone<br>
  <span class='mark'>Catalysts</span> - Powerful price patterns, they suggest what a market may do after they reach a zone<br>
  <span class='mark'>The Retouch</span> - The market trades beyond a zone before returning to the zone from the other side to confirm the importance of the zone<br>
  <span class='mark'>Two-Candlestick Formation</span> - A price pattern made up of two candlesticks. The definition and validity of the price pattern is dependent upon characteristics of both candlesticks<br>
  <span class='mark'>The Big Shadow</span> - This is a two-candlestick, reversal formation and an important catalyst for the naked trader<br>
  <span class='mark'>Kangaroo Tail</span> - Ideally, the kangaroo tail is much longer than the body of the kangaroo<br>
  <span class='mark'>Range</span> - The range of a candlestick is the distance in pips between the high and low of the candlestick<br>
  <span class='mark'>Big Belt</span> - A big belt is a very significant occurrence in the market. Big belts rarely occur, and when they do they will nearly always occur on a support and resistance zone<br>
  <span class='mark'>Trendy Kangaroo</span> - The trendy kangaroo is a special case of the kangaroo-tail trade. This is a specific method for jumping on trends<br>
  <span class='mark'>Trading Exits</span> - An exit strategy that is dynamic, with no fixed exit price. The exit price constantly adjusts with the movement in the market to maximize profit<br>
</div>

