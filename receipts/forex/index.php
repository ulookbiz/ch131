    <h3>Основные формулы</h3>
</div>  

<div class='info'>
  <h4>Как рассчитывается стоимость</h4>
  <span class='mark'>value = lots x 100,000 x price. Например: 1 x 100,000 x 1.2322 = 123,220</span><br>
  <h4>Изменение стоимости</h4>
  <span class='mark'>change = lots x 100,000 x (price1-price2)</span><br>
  <h4>Изменение стоимости в пипсах</h4>
  <span class='mark'>change = lots x 100,000 x 0.0001 x dpips = lots x 10 x dpips</span><br>
  <h4>Расчет стоимости пипса</h4>
  <span class='mark'>pip value = change/dpips = lots x 10 x dpips / dpips = lots x 10</span><br>
  <span class='mark'>(в единицах квотируемой валюты)</span><br>
