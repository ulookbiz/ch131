    <h3>Рекомендации по выбору счета</h3>
</div>  

<div class='info'>
  <ol>
    <li>Открывайте счет с плечом как можно больше, минимум 1:500, нам нужен каждый доллар свободной маржи</li>
    <li>Выбирайте тип счета с минимальным лотом 0.01: торгуя на центовых счетах, мы в 99 процентах случаев будет использовать принцип Мартингейла, а значит, стартовый лот должен быть минимальным</li>
    <li>Выбирайте счет с 5-знаком: часто бывает, что не хватает 1-го пипса, чтобы закрыть сделки по ТейкПрофит</li>
    <li>Выбирайте счет с плавающим спредом и убедитесь, что ваш советник не открывает ордера, если спред расширился больше, чем размер
ТейкПрофит</li>
    <li>Если вы выбрали счет с плавающим спредом, то будет отлично найти еще и без комиссии. Редко, но такое есть</li>
    <li>Убедитесь, что тип счета, который вы выбрали, не содержит ограничений на время сделки. Бывает, советник быстро открывает и закрывает сделку, а брокер требует быть на рынке не менее N минут</li>
    <li>Открывайте счета только в USD – долларах США</li>
  </ol>
</div>

