<div class='title'>
    <h3>Характеристика советников</h3>
</div>  

<div class='info'>
Синонимы понятия Форекс-Советник – АТС (автоматическая торговая система), Эксперт, Сова, Форекс-Робот. <br>
Основные типы советников по типу Торговой Системы (ТС) – трендовые и сеточники. Трендовые советники хорошо работают на затяжных движениях цены в одну сторону. Сеточник, наоборот, лучше работает во время флета. Известно, что 90 процентов времени рынок находится в состоянии флета. <br>
Существенное влияние на прибыльность советника оказывает система МаниМенеджмента (от английского выражения Money Management –
управление деньгами). МаниМенеджмент – это принцип, по которому советник выбирает размер лота, уровень СЛ (СтопЛосс) и ТП (ТейкПрофит). <br>Классическая модель используется в трендовых советниках: вход в рынок таким размером лота, чтобы СЛ приводил к потере не больше 2-5% депозита, а ТП был в 2-3 раза больше СЛ. <br>
У трендовых советников очень низкий процент прибыльных сделок, и это компенсируется отношением СЛ/ТП 1 к 2 или 1 к 3, чтобы перекрывать убыточные сделки.<br>
В сеточниках чаще используют усреднение и Мартингейл. Оба принципа не предполагают выставление СЛ и считаются очень рискованными, но только с таким принципом можно добиться высокой доходности.<br>
Усреднение работает так: когда цена движется не в вашу сторону, то советник открывает новый ордер против движения и пересчитывает уровень ТП, надеясь, что цена откатится, и суммарно закрытые ордера закроются в общий плюс. Принцип Мартингейла добавляет в усреднение фактор умножения каждого последующего лота на множитель.    
</div>

<div class='title'>
    <h3>Рекомендации по работе с советниками</h3>
</div>  

<div class='info'>
  <ol>
    <li>Изучите внимательно инструкцию к вашему советнику и сверьтесь с соответствием типа счета под него</li>
    <li>Скопируйте правильно файлы советника по своим папкам. Исполняемые файлы .ex4 – в папки EXPERTS, .dll в Libraries, файлы .set – в
PRESETS, шаблон – в TEMPLATES</li>
    <li>Не торгуйте разными советниками на одном счете – это нарушит статистику и не позволит вам оптимизировать торговлю</li>
    <li>Рассчитайте правильно лот для советника, используйте калькулятор Мартингейла или калькулятор лота. Для стабильной работы и для
разгона это разная величина</li>
    <li>Убедитесь, что включена автоторговля в терминале и проставлены галочки в соответствии с тем, как требует инструкция советника</li>
    <li>После старта советника через время должна вывестись техническая информация советника, если нет – нужно проверить вкладки 
«Журнал» и «Эксперты»</li>
    <li>Обязательно в стратегию заработка включайте минимум четыре счета и будьте готовы к потере 2 из 4 счетов до момента окупаемости.
То есть, если вы начали торговлю на 4 счетах по 50 долларов, нужно заложить потерю 100 долларов, а 3 и 4 счета должны окупить себя и
эту потерю</li>
    <li>Запускайте торговлю на всех счетах с разными настройками или парами. Например, от самых рискованных до более консервативных. 1
счет можно настроить на разгон депозита, 2 – на средний риск, и 3-4 – на стабильный доход</li>
    <li>Всегда держите в запасе на быстром пополнении (банковские переводы не подходят, посмотрите условия ввода у брокера и выберите
такой, где пополнение моментальное) такую же сумму, которая у вас в торговле: часто бывает так, что нужно «долить» счет, чтобы переждать
просадку и закрыться в плюс</li>
    <li>Обязательно подключите счета к мониторингу MyFXBook.com – это нужно, чтобы выделять самые удачные пары, время и настройки для
торговли советником</li>
    <li>Выводите прибыль КАЖДУЮ неделю. Обязательно. Исключение – счет для разгона, на нем можете увеличивать лот по мере роста депозита и вывести прибыль, когда будет 100 процентов. Верните свой депозит и дальше спокойно разгоняйте счет</li>
    <li>Помните: платные советники дают возможность зарабатывать и на спреде. В зависимости от советника это существенно и может удвоить
ваш доход!</li>
    <li>Проверяйте ситуацию на счетах каждый день перед открытием Лондонской торговой сессии и Нью-Йоркской!</li>
    <li>Не торгуйте 10-ю советниками, у вас будет голова кругом. Выберите 1-3 разных и оттачивайте не менее 6 месяцев торговлю на них.
Вам нужен один VPS и 4 счета, за большим количеством не уследить!</li>
    <li>Не вмешивайтесь в торговлю советника. Не закрывайте прибыльные ордера, оставляя убыточные</li>
  </ol>
</div>

<div class='title'>
    <h3>Рекомендации по случаям слива депозита</h3>
</div>  

<div class='info'>
  <ol>
    <li>Смиритесь со сливами депозитов, часть вы всегда будете терять – это нормально для любого бизнеса, главное – чтобы общий итог был в плюсе. Ведите финансовый учет в разрезе месяца. Если месяц был убыточным, проанализируйте ошибки, не завышены ли риски, нет ли ошибок в настройках советников и т.д.</li>
    <li>Изучите понятие тренда и уровней, отбитие и пробитие уровня – это позволит вам адекватно оценивать целесообразность «долива» и
вероятность разворота цены в вашу сторону</li>
    <li>Если просадка до 10 процентов, то волноваться не нужно, если просадка больше 20 процентов, то закройте убыточные ордера, которые слишком близко друг к другу, сделайте «окна» между ордерами шире</li>
    <li>Если просадка больше 50 процентов, то оцените ситуацию со знаниями из пункта 2 и примите решение о доливе</li>
    <li>Изучите локирование ордеров; это сложный инструмент, но он может спасти ваш депозит</li>
  </ol>
