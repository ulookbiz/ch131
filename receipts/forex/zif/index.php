</div>  

<div class='info'>
  <ol>
    <li>Убедитесь, что способ, которым вы будете пополнять счет у брокера, доступен для вывода. Часто бывает, что способ, которым вы ввели
средства, недоступен для вывода, и предлагаются стандартные варианты на вывод с большой комиссией. У нормальных брокеров, если такого же способа на вывод нет, вывести можно на любой из доступных. Например, ввести с карты MasterCard, а вывести на Neteller</li>
    <li>Выбирая бонусы, выбирайте только торгуемые, – те, которые участвуют в просадке, и убедитесь, что вы можете от них отказаться в лю
бой момент без ущерба к вашим личным средствам</li>
    <li>Обязательно используйте VPS-сервера. При торговле на центовых счетах Форекс-советниками нам важен быстрый доступ без потери связи и задержек на исполнение ордеров. Ищите сервера с памятью не ниже 512 мб, а лучше 1 гб, там вы сможете установить 3-5 терминалов МетаТрейдер 4</li>
    <li>Перед покупкой сервера заходите на сайт провайдера в разное время и пишите техподдержке вопросы: вам нужно знать, в любое ли время есть технический специалист на связи, иначе в самый ответственный момент, когда советник должен торговать, а сервер «упал», и техподдержка не отвечает, вы можете потерять депозит</li>
    <li>Не рекомендую использовать VPS от брокера: если он бесплатный, то в нем повышенные требования к объемам депозита и торговли, а еще на такой VPS не поставить МетаТрейдер4 от другого брокера</li>
    <li>Проверяйте VPS-сервер на работоспособность минимум раз в день</li>
    <li>Держите терминал выбранного брокера на своем личном компьютере установленным и настроенным с тем же советником и параметрами. Запишите себе в блокнот пары и Magic-номера. Если VPS «упадет», вам нужно будет быстро запустить свой локальный терминал с теми же настройками, парами и Magic-номерами</li>
    <li>Проверяйте вкладки «Эксперты» и «Журнал». Если есть ошибки, то разберитесь с причиной; если в журнале часто пишется о потере связи, то смените VPS-сервис</li>
    <li>Подпишитесь на новости брокера и следите за изменениями торговых условий</li>
    <li>Следите за экономическим календарем, например, на ru.investing.com.</li>
